fun hasDifferentDigits(n: Int): Boolean {
    var count = 0
    var div = 1
    val digit = n % 10
    if (n % 10 == n) return false
    while (digit == n / div % 10) {
        div *= 10
        count += 1
    }
    if (count == digitNumber(n)) return false
    return true
}


